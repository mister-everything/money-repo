export interface RandomNicknameOptions {
  /** 최대 글자 수 (기본값: 14) */
  maxLength?: number;
  /** 숫자 포함 여부 (기본값: false) */
  includeNumber?: boolean;
  /** 접미사 포함 여부 (기본값: false) */
  includeSuffix?: boolean;
}

/**
 * 한국어 랜덤 닉네임 생성기
 *
 * 다양한 조합 패턴 사용:
 * - 형용사 + 명사: "졸린판다", "수상한고양이"
 * - 색상 + 명사: "파란고래", "분홍토끼"
 * - 직업 + 동물: "탐정고양이", "마법사펭귄"
 * - 장소 + 동물: "우주판다", "심해상어"
 * - 의성어 + 명사: "뽀짝토끼", "몽글몽글구름"
 * - 음식 + 동물: "떡볶이고양이", "마카롱햄스터"
 * - 동물 + 음식: "고양이라면", "판다피자"
 * - 서사형: "바다에서만난여자", "별을세는소녀", "꿈의왕자"
 * - 장소의인물: "숲의마녀", "바다의소녀"
 * - 동사+인물: "꿈꾸는소녀", "잠든왕자"
 *
 * @example
 * generateRandomNickname(); // "졸린판다", "바다의소녀", "꿈꾸는왕자"
 * generateRandomNickname({ includeNumber: true }); // "수상한고양이42"
 * generateRandomNickname({ includeSuffix: true }); // "배고픈햄스터님"
 */
export function generateRandomNickname(
  options: RandomNicknameOptions = {},
): string {
  const {
    maxLength = 14,
    includeNumber = false,
    includeSuffix = false,
  } = options;

  let attempts = 0;
  const maxAttempts = 100;

  while (attempts < maxAttempts) {
    attempts++;

    // 랜덤 패턴 선택
    const pattern = pickRandom(NICKNAME_PATTERNS);
    let nickname = pattern();

    // 접미사 추가
    if (includeSuffix && Math.random() > 0.5) {
      const suffix = pickRandom(SUFFIXES);
      if ((nickname + suffix).length <= maxLength) {
        nickname += suffix;
      }
    }

    // 숫자 추가
    if (includeNumber) {
      const numberPattern = pickRandom(NUMBER_PATTERNS);
      const number = numberPattern();
      if ((nickname + number).length <= maxLength) {
        nickname += number;
      }
    }

    if (nickname.length <= maxLength) {
      return nickname;
    }
  }

  // 폴백: 짧은 조합
  const shortColors = COLORS.filter((c) => c.length <= 2);
  const shortAnimals = ANIMALS.filter((a) => a.length <= 3);

  return pickRandom(shortColors) + pickRandom(shortAnimals);
}

/**
 * 여러 개의 고유한 랜덤 닉네임 생성
 *
 * @example
 * generateUniqueNicknames(5);
 * // ["졸린판다", "파란마법사", "우주고양이", "뽀짝토끼", "심해상어"]
 */
export function generateUniqueNicknames(
  count: number,
  options: RandomNicknameOptions = {},
): string[] {
  const nicknames = new Set<string>();
  let attempts = 0;
  const maxAttempts = count * 10;

  while (nicknames.size < count && attempts < maxAttempts) {
    nicknames.add(generateRandomNickname(options));
    attempts++;
  }

  return Array.from(nicknames);
}
